package lista8;

public class Palavra {
	private String palavra;
	private String dica;
	
	public void setPalavra(String palavra) {
		this.palavra = palavra;
	}
	public void setDica(String dica) {
		this.dica = dica;
	}
	public String getPalavra() {
		return palavra;
	}
	public String getDica() {
		return dica;
	}
	
}
